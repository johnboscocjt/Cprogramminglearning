#include <stdio.h>
/*n the context of C programming, the term "ctr" is often used as an abbreviation for "counter." It is a common choice for a variable name when you want to represent a counting or looping variable, typically used in loops or iterations.*/
int main() {
	int ctr;
    // Using "ctr" as a counter variable in a loop
    for ( ctr = 0; ctr < 5; ctr++) {
        printf("Iteration %d\n", ctr);
    }

    return 0;
}

